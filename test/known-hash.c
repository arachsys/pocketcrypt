static int check(uint8_t *in, const char *hex) {
  for (uint8_t byte = 0; *hex; in++, hex += 2)
    if (sscanf(hex, "%02hhx", &byte) != 1 || byte != *in)
      return -1;
  return 0;
}

int main(void) {
  const size_t max = sizeof(known) / sizeof(char *) - 1;
  uint8_t in[max], out[32];

  for (size_t i = 0; i < max; i++)
    in[i] = (uint8_t) i;

  for (size_t length = 0; length <= max; length++) {
    duplex_t state = { 0 };
    duplex_pad(state, duplex_absorb(state, 0, in, length));
    duplex_squeeze(state, 0, out, 32);
    if (check(out, known[length])) /* variable time */
      errx(EXIT_FAILURE, "Known hash failure with %zd-byte input", length);
  }

  printf("Reference digests checked\n");
  return EXIT_SUCCESS;
}
